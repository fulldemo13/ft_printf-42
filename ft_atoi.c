#include "ft_printf.h"

int			ft_atoi(const char *str)
{
	int nb;

	nb = 0;
	while (*str >= '0' && *str <= '9')
	{
		nb = nb * 10 + (*str - '0');
		str++;
	}
	return (nb);
}
